package com.wmenjoy.utils.config.parser;

/**
 * 代码有问题
 * 
 * @author jinliang.liu
 * 
 */
public class SystemConfigErrorException extends BaseConfigException {

    /**
     * 
     */
    private static final long serialVersionUID = 1L;

    public SystemConfigErrorException() {
        super();
    }

    public SystemConfigErrorException(String message, Throwable cause) {
        super(message, cause);
    }

    public SystemConfigErrorException(String message) {
        super(message);
    }

    public SystemConfigErrorException(Throwable cause) {
        super(cause);
    }

}
